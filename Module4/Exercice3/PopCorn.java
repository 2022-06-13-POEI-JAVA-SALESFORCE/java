package exo3;

public class PopCorn {
	
	private String type;
	private int prix;
	
	public PopCorn(String type, int prix) {
		super();
		this.type = type;
		this.prix = prix;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getPrix() {
		return prix;
	}
	public void setPrix(int prix) {
		this.prix = prix;
	}
	
	

}
